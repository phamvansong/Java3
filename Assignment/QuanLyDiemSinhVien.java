    public void loadDataToTable() {
        try {
            tblModel.setRowCount(0);
            Connection conn = DriverManager.getConnection(db_url);
            Statement stmt = conn.createStatement();
            String sql = "SELECT TOP 3 STUDENTS.MASV, HOTEN, TIENGANH, TINHOC, GDTC, (TIENGANH + TINHOC + GDTC)/3 AS DIEMTB FROM GRADE, STUDENTS WHERE GRADE.MASV = STUDENTS.MASV ORDER BY DIEMTB DESC";
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getString(1));
                row.add(rs.getString(2));
                row.add(rs.getString(3));
                row.add(rs.getString(4));
                row.add(rs.getString(5));
                row.add(rs.getString(6));
                tblModel.addRow(row);
            }
            tblDiemSinhVien.setModel(tblModel);
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadData() {
        Connection conn = null;
        Statement stmt = null;
        try {
            conn = DriverManager.getConnection(db_url);
            stmt = conn.createStatement();
            String sql = "SELECT GRADE.MASV AS MASV, HOTEN, TIENGANH, TINHOC, GDTC FROM GRADE, STUDENTS WHERE GRADE.MASV = STUDENTS.MASV";
            ResultSet rs = stmt.executeQuery(sql);

            list.clear();
            while (rs.next()) {
                String MASV = rs.getString("MASV");
                String HoTen = rs.getString("HOTEN");
                int TiengAnh = rs.getInt("TIENGANH");
                int TinHoc = rs.getInt("TINHOC");
                int GDTC = rs.getInt("GDTC");
                double DiemTB = (TiengAnh + TinHoc + GDTC) / 3;

                DiemSV diemsv = new DiemSV(MASV, HoTen, TiengAnh, TinHoc, GDTC, DiemTB);
                list.add(diemsv);
            }
            currentIndex = 0;
            DisplayDiemSV();
            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void DisplayDiemSV() {
        DiemSV diemSV = list.get(currentIndex);
        txtMaSV.setText(diemSV.MaSV + "");
        lblTenSV.setText(diemSV.HoTen + "");
        txtDiemTA.setText(diemSV.TiengAnh + "");
        txtDiemTH.setText(diemSV.TinHoc + "");
        txtDiemGDTC.setText(diemSV.GDTC + "");
        UpdateDiemTB();
    }

    public void UpdateDiemTB() {
        int ta = Integer.parseInt(txtDiemTA.getText());
        int th = Integer.parseInt(txtDiemTH.getText());
        int gdtc = Integer.parseInt(txtDiemGDTC.getText());
        float tb = (ta + th + gdtc) / 3;
        lblDiemTB.setText(tb + "");
    }

