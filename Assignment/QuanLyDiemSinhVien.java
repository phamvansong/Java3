    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE GRADE SET TIENGANH = ?, TINHOC = ?, GDTC = ? WHERE MASV = ?";
        Connection conn = null;
        PreparedStatement ps = null;
        try {
            conn = DriverManager.getConnection(db_url);
            ps = conn.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtDiemTA.getText()));
            ps.setInt(2, Integer.parseInt(txtDiemTA.getText()));
            ps.setInt(3, Integer.parseInt(txtDiemTH.getText()));
            ps.setString(4, txtMaSV.getText());
            ps.execute();
            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
            loadData();
            loadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Cập nhật không thành công");
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
                if (ps != null) {
                    ps.close();
                }
            } catch (Exception e2) {
                e2.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int i;
        String masv;
        masv = txtSearchMaSV.getText();
        for (i = 0; i < list.size(); i++) {
            DiemSV diemsv = list.get(i);
            if (diemsv.MaSV.equalsIgnoreCase(masv)) {
                JOptionPane.showMessageDialog(this, "Đã tìm thấy", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                currentIndex = i;
                DisplayDiemSV();
                break;
            }
        }
        if (i == list.size()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            txtSearchMaSV.requestFocus();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

