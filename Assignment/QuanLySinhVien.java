    private void lblHinhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblHinhMouseClicked
        // TODO add your handling code here:
        JFileChooser jft = new JFileChooser();
        int result = jft.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = jft.getSelectedFile();
            String fullPath = file.getAbsolutePath();
            fileNamePictures = jft.getSelectedFile().getName();
            upHinh(fullPath);
            try {
                Path src = Paths.get(fullPath);
                Path dest = Paths.get("src\\Img\\" + fileNamePictures);
                Files.copy(src, dest, StandardCopyOption.REPLACE_EXISTING);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_lblHinhMouseClicked

