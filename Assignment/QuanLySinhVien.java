    public void Dodulieu(int row) {
//        if (row < 0) {
//            return;
//        }
        txtMaSV.setText(tblSinhVien.getValueAt(row, 0).toString());
        txtHoTen.setText(tblSinhVien.getValueAt(row, 1).toString());
        txtEmail.setText(tblSinhVien.getValueAt(row, 2).toString());
        txtSoDT.setText(tblSinhVien.getValueAt(row, 3).toString());
        int gioitinh = Integer.parseInt(tblSinhVien.getValueAt(row, 4).toString());
        if (gioitinh == 1) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtDiaChi.setText(tblSinhVien.getValueAt(row, 5).toString());
        upHinh(tblSinhVien.getValueAt(row, 6).toString());
    }

    public void upHinh(String hinh) {
        ImageIcon image = new ImageIcon("src\\Img\\" + hinh);
        Image im = image.getImage();
        ImageIcon icon = new ImageIcon(im.getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), im.SCALE_SMOOTH));
        lblHinh.setIcon(icon);
    }

