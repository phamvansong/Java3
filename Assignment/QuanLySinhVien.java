    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = DriverManager.getConnection(db_url);
            String sql = "insert into STUDENTS(MASV,HOTEN,EMAIL,SODT,GIOITINH,DIACHI,HINH) values (?,?,?,?,?,?,?)";
            PreparedStatement stm = conn.prepareStatement(sql);
            stm.setString(1, txtMaSV.getText().trim());
            stm.setString(2, txtHoTen.getText().trim());
            stm.setString(3, txtEmail.getText().trim());
            stm.setString(4, txtSoDT.getText().trim());
            String gt = "";
            if (rdoNam.isSelected()) {
                gt = "true";
            } else {
                gt = "false";
            }
            stm.setString(5, gt);
            stm.setString(6, txtDiaChi.getText().trim());
            stm.setString(7, fileNamePictures.trim());

            stm.executeUpdate();
            stm.close();
            conn.close();
            loadDataToTable();
            Dodulieu(0);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnSaveActionPerformed

