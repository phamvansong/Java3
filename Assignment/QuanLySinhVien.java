    public void loadDataToTable() {
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;
        try {
            conn = DriverManager.getConnection(db_url);
            String sql = "SELECT * FROM STUDENTS";
            stmt = conn.createStatement();
            rs = stmt.executeQuery(sql);
            Vector sv = null;
            tblSVModel.setRowCount(0);
            if (rs.isBeforeFirst() == false) {
                JOptionPane.showMessageDialog(this, "Chua co sinh vien");
                return;
            }
            while (rs.next()) {
                sv = new Vector();
                sv.add(rs.getString("MASV"));
                sv.add(rs.getString("HOTEN"));
                sv.add(rs.getString("EMAIL"));
                sv.add(rs.getString("SODT"));
                sv.add(rs.getString("GIOITINH"));
                sv.add(rs.getString("DIACHI"));
                sv.add(rs.getString("HINH"));
                tblSVModel.addRow(sv);
            }
            tblSinhVien.setModel(tblSVModel);
            conn.close();
            stmt.close();
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

